void process_character(char ch)
{
  static uint32_t strlen = 0;
  switch (ch)
  {
  case '\0':
    printf("Received: %s\r\n", receive_buffer);
    receive_buffer[0] = '\0';
    strlen = 0;
    break;
  default:
    if (strlen < BUFFER_SIZE_BYTE)
    {
      strncat((char *)&receive_buffer, &ch, 1); // Danger, Will Robinson!  this could go horribly wrong!
      ++strlen;
    }
  }
}

